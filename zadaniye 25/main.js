var str = "hAPPY bIRTHDAY";

function change_register(str) {
    var new_str = "";
    var len = str.length;
    for (var i = 0; i < len; i++) {
        if (str[i] === str[i].toLowerCase()) {
            new_str += str[i].toUpperCase();
        } else {
            new_str += str[i].toLowerCase();
        }
    }
    return new_str;
}

document.writeln(change_register(str));
